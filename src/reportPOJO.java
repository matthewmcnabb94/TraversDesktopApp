public class reportPOJO {
    
    public String Id;
    public String customerName;
    public String date;
    public String vehicleDetails;
    public String turboPartNumber;
    public String price;
    public String payment;
    public String fittingRequired;
    public String orderStartedDate;

    public String getId() {
        return Id;
    }

    public void setId(String Id) {
        this.Id = Id;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getVehicleDetails() {
        return vehicleDetails;
    }

    public void setVehicleDetails(String vehicleDetails) {
        this.vehicleDetails = vehicleDetails;
    }

    public String getTurboPartNumber() {
        return turboPartNumber;
    }

    public void setTurboPartNumber(String turboPartNumber) {
        this.turboPartNumber = turboPartNumber;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPayment() {
        return payment;
    }

    public void setPayment(String payment) {
        this.payment = payment;
    }

    public String getFittingRequired() {
        return fittingRequired;
    }

    public void setFittingRequired(String fittingRequired) {
        this.fittingRequired = fittingRequired;
    }

    public String getOrderStartedDate() {
        return orderStartedDate;
    }

    public void setOrderStartedDate(String orderStartedDate) {
        this.orderStartedDate = orderStartedDate;
    }
    
    
    
    
    
    
    
    
}
